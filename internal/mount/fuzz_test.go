package mount

import "testing"

func FuzzDecode(f *testing.F) {
	for _, s := range []string{
		"112233440000000000000002000186a50000000300000001000000000000000000000000000000000000000c2f6578706f72742f686f6d65",
		"1122334400000001000000000000000000000000000000000000000000000008aabbccdd010203040000000100000001",
		"1122334400000001000000000000000000000000000000000000000d",
		"11223344000000010000000000000000000000000000000000abcdef12",
		"112233", "0000000000000002", "", "zz",
	} {
		f.Add(s)
	}
	f.Fuzz(func(_ *testing.T, s string) { _, _ = Decode(s) })
}
