package portmap

import "testing"

func FuzzDecode(f *testing.F) {
	for _, s := range []string{
		"112233440000000000000002000186a0000000020000000300000000000000000000000000000000000186a3000000030000001100000000",
		"11223344000000010000000000000000000000000000000000000801",
		"aabbccdd000000010000000000000000000000000000000000000001000186a000000002000000110000006f00000001000186a500000003000000060000027b00000000",
		"00000001000000010000000000000000000000000000000000000000",
		"11223344000000010000000100000001",
		"112233", "0000000000000002", "", "zz",
	} {
		f.Add(s)
	}
	f.Fuzz(func(_ *testing.T, s string) { _, _ = Decode(s) })
}
