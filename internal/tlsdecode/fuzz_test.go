package tlsdecode

import "testing"

func FuzzDecodeBytes(f *testing.F) {
	for _, s := range [][]byte{
		{}, {0x16, 0x03, 0x01}, {0x16, 0x03, 0x03, 0x00, 0x05}, {0x17}, {0xFF},
		// Certificate records (type 11), exercising the cert-list walk +
		// x509decode chaining path on small/garbage cert bytes.
		{0x16, 0x03, 0x03, 0x00, 0x04, 0x0b, 0x00, 0x00, 0x01},
		{0x16, 0x03, 0x03, 0x00, 0x0a, 0x0b, 0x00, 0x00, 0x06, 0x00, 0x00, 0x03, 0x00, 0x00, 0x01},
	} {
		f.Add(s)
	}
	f.Fuzz(func(t *testing.T, b []byte) { _, _ = DecodeBytes(b) })
}
