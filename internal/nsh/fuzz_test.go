package nsh

import "testing"

func FuzzDecode(f *testing.F) {
	for _, s := range []string{
		"0fc60101123456ff000000000000000000000000000000004500001c00010000401166ce0a0000010a000002003500350008eb71",
		"0fc6010300000100000000000000000000000000000000112233445566778899aabb08004500001400010000400166f70a0000010a000002",
		"0fc60201123456ff" + "00000000000000000000000000000000" + "60000000000011401234",
		"0fc60101", "0f000101123456ff", "0fff0101123456ffaabb", "", "zz",
	} {
		f.Add(s)
	}
	f.Fuzz(func(_ *testing.T, s string) { _, _ = Decode(s) })
}
